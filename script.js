const normalizedHost = window.location.hostname.toLowerCase();
const rawConfig = siteConfigs[normalizedHost] || siteConfigs.default;
const hostConfig = rawConfig.aliasOf ? siteConfigs[rawConfig.aliasOf] : rawConfig;
const brandButtons = {
  do1ruky: document.getElementById("switch-do1ruky"),
  modrokuk: document.getElementById("switch-modrokuk"),
};

const assignText = (id, value) => {
  const node = document.getElementById(id);
  if (node) {
    node.textContent = value;
  }
};

const setActiveBrandButton = (configKey) => {
  if (brandButtons.do1ruky) {
    brandButtons.do1ruky.classList.toggle("is-active", configKey === "do1ruky.cz");
  }
  if (brandButtons.modrokuk) {
    brandButtons.modrokuk.classList.toggle("is-active", configKey === "modrokuk.cz");
  }
};

const setHrefIfExists = (selector, href) => {
  document.querySelectorAll(selector).forEach((node) => {
    node.setAttribute("href", href);
  });
};

const renderCards = (targetId, items) => {
  const target = document.getElementById(targetId);
  if (!target) {
    return;
  }

  target.innerHTML = items
    .map((item) => {
      if (typeof item === "string") {
        return `<article class="info-card"><h3>${item}</h3></article>`;
      }

      return `
        <article class="info-card">
          <h3>${item.title}</h3>
          <p>${item.text}</p>
        </article>
      `;
    })
    .join("");
};

const renderParagraphs = (targetId, items) => {
  const target = document.getElementById(targetId);
  if (!target) {
    return;
  }

  target.innerHTML = items.map((item) => `<p>${item}</p>`).join("");
};

const renderInquiryItems = (targetId, items) => {
  const target = document.getElementById(targetId);
  if (!target) {
    return;
  }

  target.innerHTML = `
    <p>Pošli:</p>
    <ul>
      ${items.map((item) => `<li>${item}</li>`).join("")}
    </ul>
  `;
};

const applyConfig = (configKey) => {
  const config = siteConfigs[configKey];
  if (!config) {
    return;
  }

  document.documentElement.style.setProperty("--accent", config.accent);
  document.documentElement.style.setProperty("--accent-dark", config.accentDark);
  document.documentElement.style.setProperty("--bg", config.background);

  assignText("brand-name", config.brand);
  assignText("domain-badge", config.badge);
  assignText("eyebrow", config.eyebrow);
  assignText("headline", config.headline);
  assignText("hero-text", config.heroText);
  assignText("card-title", config.cardTitle);
  assignText("card-text", config.cardText);
  assignText("about-title", config.aboutTitle);
  assignText("services-title", config.servicesTitle);
  assignText("events-title", config.eventsTitle);
  assignText("events-note", config.eventsNote);
  assignText("reasons-title", config.reasonsTitle);
  assignText("gallery-title", config.galleryTitle);
  assignText("gallery-text", config.galleryText);
  assignText("references-title", config.referencesTitle);
  assignText("inquiry-title", config.inquiryTitle);
  assignText("inquiry-text", config.inquiryText);
  assignText("inquiry-note", config.inquiryNote);
  assignText("contact-brand", config.brand);
  assignText("contact-email", config.email);
  assignText("contact-phone", config.phone);
  assignText("contact-page-text", config.contactPageText);
  assignText("contact-page-heading", config.contactPageHeading);
  assignText("contact-email-detail", config.email);
  assignText("contact-phone-detail", config.phone);
  assignText("contact-service-detail", config.contactServiceDetail);
  assignText("primary-cta", config.primaryCta);

  const heroImage = document.getElementById("hero-image");
  if (heroImage) {
    heroImage.src = config.heroImage;
    heroImage.alt = config.heroImageAlt;
  }

  const featurePreview = document.getElementById("feature-preview");
  if (featurePreview) {
    featurePreview.src = config.featureImage || config.heroImage;
    featurePreview.alt = config.featureImageAlt || config.heroImageAlt;
  }

  const brandLogo = document.getElementById("brand-logo");
  const brandLockup = document.querySelector(".brand-lockup");
  if (brandLogo && brandLockup) {
    if (config.brandLogo) {
      brandLogo.src = config.brandLogo;
      brandLogo.alt = config.brandLogoAlt || config.brand;
      brandLockup.classList.add("has-logo");
    } else {
      brandLogo.setAttribute("src", "");
      brandLogo.setAttribute("alt", "");
      brandLockup.classList.remove("has-logo");
    }
  }

  const contactCta = document.getElementById("contact-cta");
  if (contactCta) {
    contactCta.setAttribute("href", `mailto:${config.email}`);
    contactCta.textContent = `Napsat ${config.brand}`;
  }

  const primaryCta = document.getElementById("primary-cta");
  if (primaryCta) {
    primaryCta.setAttribute("href", config.primaryCtaHref || "#");
  }

  renderParagraphs("about-text", config.aboutParagraphs || []);
  renderCards("services-list", config.services || []);
  renderCards("events-list", (config.events || []).map((item) => ({ title: item, text: "" })));
  renderCards("reasons-list", (config.reasons || []).map((item) => ({ title: item, text: "" })));
  renderCards("references-list", (config.references || []).map((item) => ({ title: `"${item}"`, text: "" })));
  renderInquiryItems("inquiry-items", config.inquiryItems || []);

  setHrefIfExists('[data-brand-link="contact"]', `kontakt.html?brand=${encodeURIComponent(configKey)}`);
  setHrefIfExists('[data-brand-link="home"]', `index.html?brand=${encodeURIComponent(configKey)}`);

  setActiveBrandButton(configKey);
};

const brandFromQuery = new URLSearchParams(window.location.search).get("brand");
const initialConfigKey =
  brandFromQuery && siteConfigs[brandFromQuery] && !siteConfigs[brandFromQuery].aliasOf
    ? brandFromQuery
    : hostConfig.brand === "Modrokuk"
      ? "modrokuk.cz"
      : "do1ruky.cz";

applyConfig(initialConfigKey);

if (brandButtons.do1ruky) {
  brandButtons.do1ruky.addEventListener("click", () => applyConfig("do1ruky.cz"));
}

if (brandButtons.modrokuk) {
  brandButtons.modrokuk.addEventListener("click", () => applyConfig("modrokuk.cz"));
}
